export function getDailyPrompt(): string {
  const dayOfYear = Math.floor(
    (Date.now() - new Date(new Date().getFullYear(), 0, 0).getTime()) / (1000 * 60 * 60 * 24)
  )
  return JOURNAL_PROMPTS[dayOfYear % JOURNAL_PROMPTS.length]
}
